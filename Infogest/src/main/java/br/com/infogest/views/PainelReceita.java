    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        try {
            Receita r = new Receita();
            ReceitaDao receita = new ReceitaDao();

            // Validar compos obrigatórios
            if ((txtDescricao.getText().isEmpty()) || (txtValor.getText().isEmpty())) {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatórios *");
            } else {
                r.setDescricao(txtDescricao.getText());
                r.setValor(Double.parseDouble(txtValor.getText()));

                receita.AdicionarRec(r);

                txtDescricao.setText("");
                txtValor.setText("");

                listarTabela();
            }

        } catch (SQLException ex) {
            Logger.getLogger(PainelReceita.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        try {
            if (listaReceita.getSelectedRow() != -1) {
                Receita r = new Receita();
                ReceitaDao receita = new ReceitaDao();

                r.setId((int) listaReceita.getValueAt(listaReceita.getSelectedRow(), 0));

                receita.ExcluirReceita(r);

                listarTabela();
            } else {
                JOptionPane.showMessageDialog(null, "Selecione um item da lista para ser excluido!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(PainelReceita.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed


