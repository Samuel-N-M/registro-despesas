    public void listarTabela() throws SQLException{
        DefaultTableModel model = (DefaultTableModel) listaDespesa.getModel();
        model.setNumRows(0);
        
        DespDao despesaDao = new DespDao();
        
        for(Despesas d: despesaDao.listarDesp()){
            model.addRow(new Object[]{
                d.getDescricao(),
                d.getQtd(),
                d.getData(),
                d.getValor()
            });
        }
    }
