    public void listarReceita() throws SQLException {
        DefaultTableModel model = (DefaultTableModel) listaReceita.getModel();
        model.setNumRows(0);

        ReceitaDao rd = new ReceitaDao();

        for (Receitas r : rd.listarReceita()) {
            model.addRow(new Object[]{
                r.getId(),
                r.getDescricao(),
                r.getData(),
                r.getValor()
            });
        }
    }

