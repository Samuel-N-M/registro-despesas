    public void listarTabela() throws SQLException {
        DefaultTableModel model = (DefaultTableModel) listaReceita.getModel();
        model.setNumRows(0);

        ReceitaDao receita = new ReceitaDao();

        for (Receita r : receita.listarReceita()) {
            model.addRow(new Object[]{
                r.getId(),
                r.getDescricao(),
                r.getData(),
                r.getValor()
            });
        }
    }

